package korpa;

/**
 *
 * @author Asus
 */
public class Cart {
    String boja;
    int cena;
    int komada;
    int total;

    public String getBoja() {
        return boja;
    }

    public void setBoja(String boja) {
        this.boja = boja;
    }

    public int getCena() {
        return cena;
    }

    public void setCena(int cena) {
        this.cena = cena;
    }

    public int getKomada() {
        return komada;
    }

    public void setKomada(int komada) {
        this.komada = komada;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
    
    
}
